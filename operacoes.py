def soma(valor1, valor2):
    soma = valor1+valor2

    return 'O valor da soma é ',soma


def subtracao(valor1, valor2):
    subtracao = valor1-valor2
    return print(f'O valor da subtração é {subtracao}')


def multipicacao(valor1, valor2):
    multipicacao = valor1*valor2
    return print(f'O valor da multiplicação é {multipicacao}')


def divisao(valor1, valor2):
    divisao = valor1/valor2
    return print(f'O valor da divisão é {divisao}')

